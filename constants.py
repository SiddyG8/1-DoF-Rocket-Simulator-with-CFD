"""
This module contains physical constants used in the simulation.
"""
g0: float = 9.80665  # Mean gravitational acceleration at sea level (m/s^2)
R: float = 287.16  # Specific gas constant for air
p0: float = 101325  # Standard sea level pressure (Pa)
T0: float = 288.15  # Standard sea level temperature (K)
L: float = -0.0065  # Lapse rate (K/m)
GM: float = 3.986e14  # Earth's gravitational constant
RE: float = 6378137  # Earth's radius (m)
gamma: float = 1.4  # Specific heat ratio of air
rho0: float = 1.225  # Standard sea level air density (kg/m^3)
